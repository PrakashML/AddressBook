public class Contacts {
    String FirstName;
    String LastName;
    String Address;
    String City;
    String Zip;
    String PhoneNum;
    String Email;

    public Contacts(String firstName, String lastName, String address, String city, String zip, String phoneNum, String email) {
        FirstName = firstName;
        LastName = lastName;
        Address = address;
        City = city;
        Zip = zip;
        PhoneNum = phoneNum;
        Email = email;
    }
}
